// Grade problem

// students marks:

    //10 - Amazing
    //5 - Good
    //3 - poor
    //0 - fail

var StudentMark = 9

if (StudentMark == 10) {
    console.log('Amazing')
} else if (StudentMark >= 5) {
    console.log('Good')
} else if (StudentMark >= 3) {
    console.log('Poor')
} else if (StudentMark >= 0) {
    console.log('fail')
}